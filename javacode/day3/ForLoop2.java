public class ForLoop2 {

	public static void main(String[] args) {
		
		for(int y=1;y<=1;y++) {
			System.out.print("*");			
		}
		System.out.println();

		for(int y=1;y<=2;y++) {
			System.out.print("*");			
		}
		System.out.println();
		
		for(int y=1;y<=3;y++) {
			System.out.print("*");			
		}
		System.out.println();

		for(int y=1;y<=4;y++) {
			System.out.print("*");			
		}
		System.out.println();

		for(int y=1;y<=5;y++) {
			System.out.print("*");			
		}
		System.out.println();

	}

}
